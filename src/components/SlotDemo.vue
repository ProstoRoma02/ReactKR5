<script setup>
import { ref, computed, reactive } from 'vue'

// Props
const props = defineProps({
  theme: {
    type: String,
    default: 'default'
  },
  showSidebar: {
    type: Boolean,
    default: false
  }
})

// –†–µ–∞–∫—Ç–∏–≤–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
const currentUser = reactive({
  id: 1,
  name: '–ò–≤–∞–Ω –ü–µ—Ç—Ä–æ–≤',
  email: 'ivan@example.com',
  avatar: 'üë®‚Äçüíª'
})

const isUserOnline = ref(true)

const sidebarData = ref([
  { id: 1, title: '–≠–ª–µ–º–µ–Ω—Ç 1', active: true },
  { id: 2, title: '–≠–ª–µ–º–µ–Ω—Ç 2', active: false },
  { id: 3, title: '–≠–ª–µ–º–µ–Ω—Ç 3', active: true }
])

const demoItems = ref([
  { id: 1, name: 'Vue.js', icon: 'üü¢' },
  { id: 2, name: 'React', icon: 'üîµ' },
  { id: 3, name: 'Angular', icon: 'üî¥' },
  { id: 4, name: 'Svelte', icon: 'üü†' }
])

const selectedItems = ref([1, 3])

const dynamicSlots = ref(['custom1', 'custom2'])

// –í—ã—á–∏—Å–ª—è–µ–º—ã–µ —Å–≤–æ–π—Å—Ç–≤–∞
const usedSlotsCount = computed(() => {
  const slots = ['header', 'default', 'footer', 'sidebar', 'user', 'items']
  return slots.filter(slot => {
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ —Å–ª–æ—Ç–∞ —á–µ—Ä–µ–∑ $slots
    return true // –£–ø—Ä–æ—â–µ–Ω–Ω–∞—è –ø—Ä–æ–≤–µ—Ä–∫–∞ –¥–ª—è –¥–µ–º–æ
  }).length
})

const availableSlots = computed(() => {
  return ['header', 'default', 'footer', 'sidebar', 'user', 'items', ...dynamicSlots.value]
})

// –ú–µ—Ç–æ–¥—ã –¥–ª—è scoped —Å–ª–æ—Ç–æ–≤
const updateUser = (updates) => {
  Object.assign(currentUser, updates)
}

const toggleSidebarItem = (id) => {
  const item = sidebarData.value.find(item => item.id === id)
  if (item) {
    item.active = !item.active
  }
}

const addSidebarItem = (title) => {
  const newId = Math.max(...sidebarData.value.map(item => item.id)) + 1
  sidebarData.value.push({
    id: newId,
    title,
    active: false
  })
}

const toggleItemSelection = (id) => {
  const index = selectedItems.value.indexOf(id)
  if (index > -1) {
    selectedItems.value.splice(index, 1)
  } else {
    selectedItems.value.push(id)
  }
}

const selectAllItems = () => {
  if (selectedItems.value.length === demoItems.value.length) {
    selectedItems.value = []
  } else {
    selectedItems.value = demoItems.value.map(item => item.id)
  }
}

const clearSelection = () => {
  selectedItems.value = []
}

// –ü–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏–µ —Å—Ç–∞—Ç—É—Å–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∫–∞–∂–¥—ã–µ 5 —Å–µ–∫—É–Ω–¥ –¥–ª—è –¥–µ–º–æ–Ω—Å—Ç—Ä–∞—Ü–∏–∏
setInterval(() => {
  isUserOnline.value = !isUserOnline.value
}, 5000)
</script>

